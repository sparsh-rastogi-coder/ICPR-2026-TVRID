import argparse
import csv
from collections import defaultdict
from typing import Dict, List, Tuple


# SCENARIOS = ("global", "same_cam_cross_passage", "up_down_cross_passage", "flat_vs_others")
SCENARIOS = ("same_cam_cross_passage", "up_down_cross_passage", "flat_vs_others")


def get_args():
    parser = argparse.ArgumentParser(description="Score ranked lists with mAP and CMC")
    parser.add_argument("--rankings", required=True, help="Rankings CSV generated by eval_generate.py")
    parser.add_argument(
        "--secret-map",
        default="data/DB_extracted/test_secret_map.csv",
        help="CSV mapping public gallery_id to true person_id and metadata",
    )
    parser.add_argument("--k", nargs="+", type=int, default=[1, 5, 10], help="CMC cutoffs")
    parser.add_argument(
        "--scenarios",
        nargs="+",
        default=list(SCENARIOS),
        choices=list(SCENARIOS),
        help="Evaluation scenarios to compute",
    )
    return parser.parse_args()


def load_secret_map(path: str) -> Dict[str, Dict[str, str]]:
    meta = {}
    with open(path, newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            meta[row["public_gallery_id"]] = {
                "person_id": row["person_id"],
                "cam_name": row.get("cam_name", ""),
                "passage_name": row.get("passage_name", ""),
                "passage_id": row.get("passage_id", ""),
                "orig_path": row.get("orig_path", ""),
            }
    return meta


def load_rankings(path: str) -> List[Dict[str, str]]:
    rows: List[Dict[str, str]] = []
    with open(path, newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            row["rank"] = int(row["rank"])
            row["distance"] = float(row["distance"])
            rows.append(row)
    # sort inside each query by rank to be safe
    rows.sort(key=lambda r: (r["query_gallery_id"], r["rank"]))
    return rows


def filter_and_rerank(rows: List[Dict[str, str]], meta: Dict[str, Dict[str, str]], scenario: str):
    grouped: Dict[str, List[Dict[str, str]]] = defaultdict(list)

    for r in rows:
        qid = r["query_gallery_id"]
        gid = r["gallery_id"]
        mq = meta.get(qid)
        mg = meta.get(gid)
        if mq is None or mg is None:
            continue

        allowed = False
        if scenario == "global":
            allowed = True
        elif scenario == "same_cam_cross_passage":
            allowed = mq["cam_name"] == mg["cam_name"] and mq["passage_name"] != mg["passage_name"]
        elif scenario == "up_down_cross_passage":
            cross_up_down = {
                (mq["cam_name"], mg["cam_name"]),
                (mg["cam_name"], mq["cam_name"]),
            }
            allowed = (
                ("upward", "downward") in cross_up_down and mq["passage_name"] != mg["passage_name"]
            )
        elif scenario == "flat_vs_others":
            # One side flat, the other not flat; different passages
            cams = {mq["cam_name"], mg["cam_name"]}
            allowed = "flat" in cams and len(cams) == 2 and mq["passage_name"] != mg["passage_name"]
        else:
            raise ValueError(f"Unknown scenario {scenario}")

        if allowed:
            grouped[qid].append(r)

    filtered: List[Dict[str, str]] = []
    for qid, entries in grouped.items():
        entries_sorted = sorted(entries, key=lambda r: r["rank"])
        for new_rank, r in enumerate(entries_sorted, start=1):
            new_r = dict(r)
            new_r["rank"] = new_rank
            filtered.append(new_r)

    filtered.sort(key=lambda r: (r["query_gallery_id"], r["rank"]))
    return filtered


def compute_metrics(rows: List[Dict[str, str]], meta: Dict[str, Dict[str, str]], cmc_cutoffs: List[int]) -> Tuple[Dict[str, float], float, int]:
    grouped = defaultdict(list)
    for r in rows:
        grouped[r["query_gallery_id"]].append(r)

    cmc_hits = {k: 0 for k in cmc_cutoffs}
    ap_list = []
    valid_queries = 0

    for qid, entries in grouped.items():
        if qid not in meta:
            continue
        q_pid = meta[qid]["person_id"]

        # positives set
        positives = [e for e in entries if meta.get(e["gallery_id"], {}).get("person_id") == q_pid and e["gallery_id"] != qid]
        if len(positives) == 0:
            # print(f"Warning: query {qid} has no valid positives; skipping")
            continue
        valid_queries += 1

        # CMC: rank of first correct match
        first_hit_rank = None
        for e in entries:
            if meta.get(e["gallery_id"], {}).get("person_id") == q_pid and e["gallery_id"] != qid:
                first_hit_rank = e["rank"]
                break
        if first_hit_rank is not None:
            for k in cmc_cutoffs:
                if first_hit_rank <= k:
                    cmc_hits[k] += 1

        # mAP
        hit_count = 0
        precision_sum = 0.0
        for idx, e in enumerate(entries, start=1):
            if meta.get(e["gallery_id"], {}).get("person_id") == q_pid and e["gallery_id"] != qid:
                hit_count += 1
                precision_sum += hit_count / idx
        ap = precision_sum / len(positives)
        ap_list.append(ap)

    cmc = {f"CMC@{k}": cmc_hits[k] / valid_queries for k in cmc_cutoffs} if valid_queries else {}
    map_score = sum(ap_list) / len(ap_list) if ap_list else 0.0
    return cmc, map_score, valid_queries


def main():
    args = get_args()
    meta = load_secret_map(args.secret_map)
    rankings = load_rankings(args.rankings)
    for scenario in args.scenarios:
        rows = filter_and_rerank(rankings, meta, scenario)
        cmc, map_score, n_queries = compute_metrics(rows, meta, args.k)

        print(f"\nScenario: {scenario}")
        print(f"Queries evaluated: {n_queries}")
        print(f"mAP: {map_score:.4f}")
        for k in sorted(cmc):
            print(f"{k}: {cmc[k]:.4f}")


if __name__ == "__main__":
    main()
